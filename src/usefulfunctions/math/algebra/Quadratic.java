package usefulfunctions.math.algebra;

public class Quadratic 
{
	private double a, b, c;
	
	public Quadratic(double a, double b, double c)
	{
		this.a = a;
		this.b = b;
		this.c = c;
	}
	
	public double discriminant()
	{
		return ((b * b) - (4 * a * c));
	}
}
